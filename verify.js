const verify = async () => {
  const account = await web3.eth.accounts.wallet.add(
    "0xb5b18af7f8145e87cc4942cb5f74af06d169418fa535f8504ca3fa23c0e20c91"
  );
  const messageHash = "1"; // web3.sha3("2");
  //   const auth = await web3.eth.personal.sign(messageHash, account.address);
  const auth = web3.eth.accounts.sign(messageHash, account.privateKey);
  console.log(auth);
  const signer = await web3.eth.accounts.recover(messageHash, auth.signature);
  const balance = await contract.methods.balanceOf(signer, messageHash).call();

  return balance > 0;
};
